package com.Referece;

public class A {
	private int y;
	private B b;
	
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public B getB() {
		return b;
	}
	public void setB(B b) {
		this.b = b;
	}
	@Override
	public String toString() {
		return "A [y=" + y + ", b=" + b + "]";
	}

	
	
}
